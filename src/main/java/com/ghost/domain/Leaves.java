package com.ghost.domain;

import java.util.Date;

public class Leaves {
	//这是请假申请表
	private int leave_id;
	private int emp_id;
	private Date beginDate;
	private Date endDate;
	private int days;
	private String type;
	private String reason;
	private Date applicationDate;
	private String approvalStatus;
	private Date approvalDate;
	private int approval_emp_id;
	private String comment;
	
	public Leaves() {
		super();
	}

	public int getLeave_id() {
		return leave_id;
	}

	public void setLeave_id(int leave_id) {
		this.leave_id = leave_id;
	}

	public int getEmp_id() {
		return emp_id;
	}

	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}

	public Date getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public Date getApplicationDate() {
		return applicationDate;
	}

	public void setApplicationDate(Date applicationDate) {
		this.applicationDate = applicationDate;
	}

	public String getApprovalStatus() {
		return approvalStatus;
	}

	public void setApprovalStatus(String approvalStatus) {
		this.approvalStatus = approvalStatus;
	}

	public Date getApprovalDate() {
		return approvalDate;
	}

	public void setApprovalDate(Date approvalDate) {
		this.approvalDate = approvalDate;
	}

	public int getApproval_emp_id() {
		return approval_emp_id;
	}

	public void setApproval_emp_id(int approval_emp_id) {
		this.approval_emp_id = approval_emp_id;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public Leaves(int leave_id, int emp_id, Date beginDate, Date endDate,
			int days, String type, String reason, Date applicationDate,
			String approvalStatus, Date approvalDate, int approval_emp_id,
			String comment) {
		super();
		this.leave_id = leave_id;
		this.emp_id = emp_id;
		this.beginDate = beginDate;
		this.endDate = endDate;
		this.days = days;
		this.type = type;
		this.reason = reason;
		this.applicationDate = applicationDate;
		this.approvalStatus = approvalStatus;
		this.approvalDate = approvalDate;
		this.approval_emp_id = approval_emp_id;
		this.comment = comment;
	}
	
	
}
